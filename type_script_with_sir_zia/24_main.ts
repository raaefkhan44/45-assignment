// Define varaibles
let apple = "apple";
let uppercaseAPPLE = "APPLE";
let ten = 10;
let twenty = 20;  

// Test for equality and inequality with Strings
console.log("Is apple equal to apple ?");
console.log(apple == "apple");

console.log("\nIs apple is not  equal to apple ?");
console.log(apple != "apple");

// Test using lowercase function
console.log("Is APPLE equal to apple after converting to lowercase?");
console.log(uppercaseAPPLE.toLowerCase() == "apple");

console.log("Is APPLE is not  equal to apple after converting to lowercase?");
console.log(uppercaseAPPLE.toLowerCase() != "apple");

// Numerical Test
// Equals to
console.log("Is ten is equal to twenty?");
console.log(ten == twenty);

// Not Equals to  
console.log("Is ten is not equal to twenty?");
console.log(ten != twenty);

// Greater than
console.log("Is ten is greater than Zero?");
console.log(ten > 0);

// Smaller than  
console.log("Is twenty is smaller than ten?");
console.log(twenty < ten);